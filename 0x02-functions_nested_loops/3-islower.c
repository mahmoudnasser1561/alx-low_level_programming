/**
 * _islower -  is a functionn that checks if the number is lower
 * @c: it's the character passed to the func as an argument
 * Description: it checks and compares between nums
 * Return: return 1 if True otherwise 0
 */
int _islower(int c)
{
	if (c >= 97 && c <= 122)
	{
		return (1);
	}
		return (0);
}
